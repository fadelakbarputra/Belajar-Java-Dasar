public class TipeDataNumber {
    public static void main(String[] args) {
        //Integer
        byte iniByte = 100; //maks = 127
        short iniShort = 1000; //maks = 32767
        int iniInt = 10000; //maks = 2 miliar
        long iniLong = 10000000L;
        //Float
        float iniFloat = 10.20F;
        double iniDouble = 10.20;
        //Underscore (mempermudah membaca)
        long harga = 1_000_000_000;
        int utang = 60000;
    }
}
