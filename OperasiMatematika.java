public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 100;
        System.out.println(a += 10);
        System.out.println(a -= 10);
        System.out.println(a *= 10);
        System.out.println(a /= 10);
        System.out.println(a %= 10);
        a--;
        System.out.println(a);
        a++;
        System.out.println(a);
    }
}
